#include <unistd.h>
#include <fcntl.h>

void	file_read(int fd)
{
	int		by;
	char	buf[200];

	by = 1;
	while (by > 0)
	{
		by = read(fd, buf, 200);
		write(1, &buf, by);
	}
}
